void main() {
  final bytes = <int>[
    0x00, 0x01, 0x00, 0x63, 0x6f, 0x6e, 0x74, 0x65, 0x6e, 0x74, 0x2f,
    0x62, 0x66, 0x62, 0x34, 0x30, 0x32, 0x31, 0x35, 0x2d, 0x65, 0x30,
    0x36, 0x65, 0x2d, 0x34, 0x38, 0x62, 0x64, 0x2d, 0x62, 0x65, 0x65,
    0x35, 0x2d, 0x38, 0x61, 0x65, 0x65, 0x61, 0x63, 0x34, 0x39, 0x66, 0x35, 0x34, 0x36,
    0x00, 0xff, 0xff
  ];
  
  final str = String.fromCharCodes(bytes);
  print('String length: \${str.length}');
  
  final matches = RegExp(r'content/[a-zA-Z0-9-]+').allMatches(str);
  print('Matches: \${matches.length}');
  for (final m in matches) {
    print(m.group(0));
  }
}
